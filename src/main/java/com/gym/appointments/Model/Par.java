package com.gym.appointments.Model;

public class Par {

    int parInicial;
    int parFinal;

    public Par() {
    }

    public Par(int parInicial, int parFinal) {
        this.parInicial = parInicial;
        this.parFinal = parFinal;
    }

    public int getParInicial() {
        return parInicial;
    }

    public void setParInicial(int parInicial) {
        this.parInicial = parInicial;
    }

    public int getParFinal() {
        return parFinal;
    }

    public void setParFinal(int parFinal) {
        this.parFinal = parFinal;
    }
}
